pub fn print_tokens(t: &[usize]) {
    let mut cap = true;

    for (i, tok) in t.iter().enumerate() {
        let mut s = TOKEN_TO_STR[*tok].to_string();

        if cap {
            s = s.chars().nth(0).unwrap().to_uppercase().to_string() + &s[1..];
        }

        cap = *tok >= 251;

        if i != 0 && !cap {
            print!(" ");
        }

        print!("{s}");
    }

    println!();
}
